class Animation:
    def __init__(self, tag, path, movement):
        self.tag = tag
        self.movement = movement
        if path == "Gestures":
            self.path = "animations/Stand/Gestures/"+ tag
        elif path == "Waiting":
            self.path = "animations/Stand/Waiting/"+ tag
        elif path =="Emotions/Negative":
            self.path = "animations/Stand/Emotions/Negative/" + tag
        elif path =="Emotions/Neutral":
            self.path = "animations/Stand/Emotions/Neutral/" + tag
        elif path =="Emotions/Positive":
            self.path = "animations/Stand/Emotions/Positive/" + tag
        elif path == "Movements":
            self.path = path
        else:
            self.path = None
    
    def __str__(self):
        return "{self.tag}"
    
def getIndex(animations, tag):
    for i in animations:
        if i.tag==tag:
            return animations.index(i)
    return -1

